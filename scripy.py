def hex_to_binary(hex_data):
    """将十六进制数据转换为二进制矩阵，矩阵大小为64x128"""
    binary_data = ''
    for byte in hex_data:
        # 将每个十六进制字节转换为8位二进制并拼接
        binary_data += f'{byte:08b}'
    
    # 将二进制数据转换为64行，每行128列的矩阵
    rows = 128
    cols = 64
    binary_matrix = [binary_data[i * cols:(i + 1) * cols] for i in range(rows)]
    
    return binary_matrix

def find_bounding_box(binary_matrix):
    """计算包含所有非零比特的最小包围矩形"""
    min_row, max_row = 128, -1
    min_col, max_col = 64, -1
    
    for r in range(128):
        for c in range(64):
            if binary_matrix[r][c] == '1':  # 找到非零比特
                min_row = min(min_row, r)
                max_row = max(max_row, r)
                min_col = min(min_col, c)
                max_col = max(max_col, c)
    
    return (min_row, max_row, min_col, max_col)

def extract_and_convert_bounding_box(binary_matrix, min_row, max_row, min_col, max_col):
    """提取最小包围矩形并将其转换为16进制"""
    new_matrix = []
    for r in range(min_row, max_row + 1):
        row_data = binary_matrix[r][min_col:max_col + 1]
        # 如果列数不是8的倍数，进行补零操作
        if len(row_data) % 8 != 0:
            row_data += '0' * (8 - len(row_data) % 8)
        new_matrix.append(row_data)
    
    # 转换为16进制输出
    hex_output = []
    for row in new_matrix:
        for i in range(0, len(row), 8):
            byte = row[i:i + 8]
            hex_output.append(f'0x{int(byte, 2):02X}')
    
    return hex_output

def process_image_data(hex_data):
    """主处理函数：转换数据并输出最小包围矩形及其偏移坐标和大小"""
    # 步骤 1：转换为二进制矩阵
    binary_matrix = hex_to_binary(hex_data)
    
    # 步骤 2：计算最小包围矩形
    min_row, max_row, min_col, max_col = find_bounding_box(binary_matrix)
    
    # 计算偏移和新的宽高
    offset_x, offset_y = min_col, min_row
    new_width = max_col - min_col + 1
    new_height = max_row - min_row + 1
    
    # 步骤 3：提取最小包围矩形并转为16进制
    hex_output = extract_and_convert_bounding_box(binary_matrix, min_row, max_row, min_col, max_col)
    
    return offset_x, offset_y, new_width, new_height, hex_output

